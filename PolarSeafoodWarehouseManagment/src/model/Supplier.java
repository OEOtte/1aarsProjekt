package model;

public class Supplier {
	private String name;
	private String phoneNo;
	private String email;
	private String country;
	
	public Supplier(String name, String phoneNo, String email, String country) {
		super();
		this.name = name;
		this.phoneNo = phoneNo;
		this.email = email;
		this.country = country;
	}
	
}
